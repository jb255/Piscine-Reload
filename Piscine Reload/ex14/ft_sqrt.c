int	ft_sqrt(int nb)
{
	int x;

	x = 0;
	if (x == nb)
		return (0);
	while (x < nb)
	{
		if (x * x == nb)
			return (x);
		else if (x * x > nb)
			return (0);
		x++;
	}
	return (x);
}
